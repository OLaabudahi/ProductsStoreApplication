package ucas.edu.android.productsstoreapplication;

public class all_sales_custom_objects {
    String sale_name , date_time , total_price ;

    public all_sales_custom_objects(String sale_name, String date_time, String total_price) {
        this.sale_name = sale_name;
        this.date_time = date_time;
        this.total_price = total_price;
    }
}
